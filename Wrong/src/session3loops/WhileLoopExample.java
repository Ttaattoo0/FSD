package session3loops;

public class WhileLoopExample {
    public static void main(String[] args) {
        int i = 1; // Initialization
        
        while (i <+ 10) { // Condition
            System.out.println("Number: " + i);
            i++; // Increment
        }
        System.out.println("Final value of i:" +i);
    }
}
